package Bean;

public class Position_Bean {
	public int x;
	public int y;

	public Position_Bean(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	

	public Position_Bean() {
		super();
	}



	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
